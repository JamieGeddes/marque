#!/usr/bin/env node
/**
 * Bake an accurate "body colour" per car into src/data/proxyColors.ts by
 * RENDERING each model (system Chrome via Playwright) under the app's HDRI and
 * sampling the dominant paint colour from the rendered car — far more reliable
 * than averaging the baked texture atlas. The distant Concours proxies
 * (ConcoursProxyField) are tinted with these so a silhouette already wears the
 * car's colour from the first frame, before the heavy GLB streams in.
 *
 *   Usage: node scripts/render-sample-colors.mjs
 *
 * Override any car by hand via model.proxyColor in src/data/cars.ts (takes
 * precedence over this baked value).
 */
import { spawn } from 'node:child_process'
import { readdir, writeFile } from 'node:fs/promises'
import { chromium } from 'playwright-core'

const PORT = 5199
const BASE = `http://localhost:${PORT}`
const OUT = 'src/data/proxyColors.ts'

function startVite() {
  const proc = spawn('npx', ['vite', '--port', String(PORT), '--strictPort'], {
    stdio: 'ignore',
  })
  return proc
}

async function waitForServer(url, timeoutMs = 30000) {
  const start = Date.now()
  while (Date.now() - start < timeoutMs) {
    try {
      const res = await fetch(url)
      if (res.ok) return
    } catch {
      // not up yet
    }
    await new Promise((r) => setTimeout(r, 300))
  }
  throw new Error('vite did not start in time')
}

const vite = startVite()
let browser
try {
  await waitForServer(`${BASE}/scripts/color-sampler.html`)

  browser = await chromium.launch({
    channel: 'chrome',
    headless: true,
    args: [
      '--use-gl=angle',
      '--use-angle=swiftshader',
      '--enable-unsafe-swiftshader',
      '--ignore-gpu-blocklist',
    ],
  })
  const page = await browser.newPage()
  page.setDefaultTimeout(60000)

  const ids = (await readdir('public/models'))
    .filter((f) => f.endsWith('.glb'))
    .map((f) => f.replace(/\.glb$/, ''))
    .sort()

  const colors = []
  for (const id of ids) {
    try {
      await page.goto(`${BASE}/scripts/color-sampler.html?model=${id}`, { waitUntil: 'load' })
      await page.waitForFunction(() => window.__sample !== undefined, { timeout: 60000 })
      const sample = await page.evaluate(() => window.__sample)
      if (sample.ok) {
        colors.push([id, sample.color])
        console.log(`${id} -> ${sample.color}`)
      } else {
        console.warn(`${id} -> FAILED: ${sample.error}`)
      }
    } catch (err) {
      console.warn(`${id} -> ERROR: ${err.message}`)
    }
  }

  const body = colors.map(([id, hex]) => `  '${id}': '${hex}',`).join('\n')
  const out = `// AUTO-GENERATED by scripts/render-sample-colors.mjs — do not edit by hand.
// Body colours for the distant Concours proxies, sampled by rendering each
// model under the app's HDRI lighting. Override per car via model.proxyColor.
export const PROXY_COLORS: Record<string, string> = {
${body}
}
`
  await writeFile(OUT, out)
  console.log(`\nWrote ${colors.length}/${ids.length} colours to ${OUT}`)
} finally {
  if (browser) await browser.close()
  vite.kill()
}
